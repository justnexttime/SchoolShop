package com.example.schoolshop.entity;

public class Goods {

    String  Goodid;
    String  GoodName;
    String  GoodImg;
    String  GoodPrice;
    String  GoodUser;
    String  GoodState;
    String  GoodKind;

    public String getGoodid() {
        return Goodid;
    }

    public void setGoodid(String goodid) {
        Goodid = goodid;
    }

    public String getGoodName() {
        return GoodName;
    }

    public void setGoodName(String goodName) {
        GoodName = goodName;
    }

    public String getGoodImg() {
        return GoodImg;
    }

    public void setGoodImg(String goodImg) {
        GoodImg = goodImg;
    }

    public String getGoodPrice() {
        return GoodPrice;
    }

    public void setGoodPrice(String goodPrice) {
        GoodPrice = goodPrice;
    }

    public String getGoodUser() {
        return GoodUser;
    }

    public void setGoodUser(String goodUser) {
        GoodUser = goodUser;
    }

    public String getGoodState() {
        return GoodState;
    }

    public void setGoodState(String goodState) {
        GoodState = goodState;
    }

    public String getGoodKind() {
        return GoodKind;
    }

    public void setGoodKind(String goodKind) {
        GoodKind = goodKind;
    }
}
